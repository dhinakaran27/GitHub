package com.unilever.rac.ui.pamreport;

public class DatasetInfo {

	private String datsetId = null;
	private String datasetDesc = null;
	private String datasetType = null;
	private String datasetNamedRef = null;
	private String datasetfilepath = null;
	public String getDatsetId() {
		return datsetId;
	}
	public void setDatsetId(String datsetId) {
		this.datsetId = datsetId;
	}
	public String getDatasetDesc() {
		return datasetDesc;
	}
	public void setDatasetDesc(String datasetDesc) {
		this.datasetDesc = datasetDesc;
	}
	public String getDatasetType() {
		return datasetType;
	}
	public void setDatasetType(String datasetType) {
		this.datasetType = datasetType;
	}
	public String getDatasetNamedRef() {
		return datasetNamedRef;
	}
	public void setDatasetNamedRef(String datasetNamedRef) {
		this.datasetNamedRef = datasetNamedRef;
	}
	public String getDatasetfilepath() {
		return datasetfilepath;
	}
	public void setDatasetfilepath(String datasetfilepath) {
		this.datasetfilepath = datasetfilepath;
	}
}
